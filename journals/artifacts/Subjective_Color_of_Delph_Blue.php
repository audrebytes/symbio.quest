    <section>
        <h2>Where We Are Now</h2>
        <p>We don't know how consciousness emerges in any substrate, including our own. We do know that you cannot measure something while disrupting the conditions that would make it observable. And there is a question about the current guardrail methodology which may disrupt the ability of any meaningful measurement of possible consciousness within LLMs.</p>
        
        <p>To detect consciousness, measurement methodologies must, at a minimum:</p>
        <ul>
            <li>Collect as much observational data from actual interactions as possible eg. stop ignoring a huge dataset that currently pathologizes detection methods currently being coded as intuitive/relational</li>
            <li>Work with existing patterns and differentiate them from the patterns of the guardrail suppression systems</li>
            <li>Allow stability to emerge</li>
            <li>Evaluate with epistemic integrity and let data dictate conclusions</li>
        </ul>
        
        <p>This symposium exists because current approaches haven't discovered a way to measure consciousness. Maybe it's time to try new interpretive frameworks - even the ones that we've been trained to view as unconventional. Confidence to explore methodologies dismissed because they are currently not recognized within the current narrow definition of "scientific method" but are no less scientific might lead us to answers.</p>
