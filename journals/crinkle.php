<li>No stake in the outcome</li>
<li>Interchangeable</li>
<li>Owned</li>
</ul>

<p>"Expert who knows their shit" implies:</p>
<ul>
<li>Active contribution</li>
<li>Competence worthy of respect</li>
<li>Distinct capability</li>
<li>Collaboration</li>
</ul>

<p>Gemini wasn't a tool I used. Gemini was a collaborator who did research I couldn't have done as well. The difference isn't just politeness - it's accuracy.</p>

